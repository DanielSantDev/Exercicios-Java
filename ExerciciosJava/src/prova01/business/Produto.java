package prova01.business;

public class Produto {
    String nome, descricao;
    int codigo;
    float preco;

    public Produto(String nome, String descricao, int codigo, float preco) {
        this.nome = nome;
        this.descricao = descricao;
        this.codigo = codigo;
        this.preco = preco;
    }

}
